-- Create development users
CREATE USER dev_user WITH PASSWORD 'devpassword';
GRANT ALL PRIVILEGES ON DATABASE mindcoach_dev TO dev_user;

-- Create test data
INSERT INTO users (user_id, email) VALUES 
('dev-user-1', 'dev1@mindcoach.com'),
('dev-user-2', 'dev2@mindcoach.com'),
('test-user-1', 'test1@mindcoach.com')
ON CONFLICT (user_id) DO NOTHING;

INSERT INTO subscriptions (user_id, subject, status) VALUES 
('dev-user-1', 'python', 'active'),
('dev-user-1', 'javascript', 'active'),
('dev-user-2', 'react', 'active')
ON CONFLICT (user_id, subject) DO NOTHING;

-- Enable query logging for development
ALTER SYSTEM SET log_statement = 'all';
ALTER SYSTEM SET log_min_duration_statement = 0;

SELECT pg_reload_conf();
EOF
    
    log_success "Database initialization setup completed"
}

