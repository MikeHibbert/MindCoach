# Create development scripts
create_dev_scripts() {
    log_info "Creating development helper scripts..."
    
    # Start development environment
    cat > scripts/dev-start.sh << 'EOF'
#!/bin/bash
echo "🚀 Starting MindCoach development environment..."

# Load environment variables
if [ -f .env.dev ]; then
    export $(cat .env.dev | grep -v '^#' | xargs)
fi

# Start development services
docker-compose -f docker-compose.dev.yml up -d

echo "✅ Development environment started!"
echo ""
echo "🌐 Services:"
echo "  Frontend:          http://localhost:3000"
echo "  Backend API:       http://localhost:5000"
echo "  Development Dashboard: http://localhost:8080"
echo "  pgAdmin:           http://localhost:8081 (dev@mindcoach.com / devpassword)"
echo "  Redis Commander:   http://localhost:8082 (dev / devpassword)"
echo ""
echo "🔧 Debugging:"
echo "  Backend Debug Port: 5678 (VS Code/PyCharm)"
echo ""
echo "📋 Useful commands:"
echo "  View logs:         docker-compose -f docker-compose.dev.yml logs -f [service]"
echo "  Stop services:     docker-compose -f docker-compose.dev.yml down"
echo "  Restart service:   docker-compose -f docker-compose.dev.yml restart [service]"
EOF
    
    # Stop development environment
    cat > scripts/dev-stop.sh << 'EOF'
#!/bin/bash
echo "🛑 Stopping MindCoach development environment..."

docker-compose -f docker-compose.dev.yml down

echo "✅ Development environment stopped!"
EOF
    
    # Reset development environment
    cat > scripts/dev-reset.sh << 'EOF'
#!/bin/bash
echo "🔄 Resetting MindCoach development environment..."

# Stop and remove containers
docker-compose -f docker-compose.dev.yml down -v

# Remove development data
rm -rf data/* users/* logs/*

# Restart environment
./scripts/dev-start.sh

echo "✅ Development environment reset completed!"
EOF
    
    # Make scripts executable
    chmod +x scripts/dev-start.sh scripts/dev-stop.sh scripts/dev-reset.sh
    
    log_success "Development scripts created"
}

