#!/bin/bash

# Development environment setup script
# Usage: ./scripts/setup-dev.sh

set -e

echo "🛠️ Setting up MindCoach development environment..."

# Color codes
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check system requirements
check_requirements() {
    log_info "Checking system requirements..."
    
    # Check Docker
    if ! command -v docker &> /dev/null; then
        log_error "Docker is required but not installed"
        log_info "Install Docker: https://docs.docker.com/get-docker/"
        exit 1
    fi
    
    # Check Docker Compose
    if ! command -v docker-compose &> /dev/null; then
        log_error "Docker Compose is required but not installed"
        log_info "Install Docker Compose: https://docs.docker.com/compose/install/"
        exit 1
    fi
    
    # Check if Docker is running
    if ! docker info &> /dev/null; then
        log_error "Docker is not running. Please start Docker and try again."
        exit 1
    fi
    
    # Check Node.js (optional, for local development)
    if command -v node &> /dev/null; then
        NODE_VERSION=$(node --version)
        log_info "Node.js version: $NODE_VERSION"
    else
        log_warning "Node.js not found. You can still use Docker for development."
    fi
    
    # Check Python (optional, for local development)
    if command -v python3 &> /dev/null; then
        PYTHON_VERSION=$(python3 --version)
        log_info "Python version: $PYTHON_VERSION"
    else
        log_warning "Python3 not found. You can still use Docker for development."
    fi
    
    log_success "System requirements check completed"
}

# Create development environment file
create_dev_env() {
    log_info "Creating development environment configuration..."
    
    if [ ! -f ".env.dev" ]; then
        cat > .env.dev << EOF
# Development Environment Configuration
COMPOSE_PROJECT_NAME=mindcoach-dev

# Database
POSTGRES_PASSWORD=devpassword
DATABASE_URL=postgresql://postgres:devpassword@postgres-dev:5432/mindcoach_dev

# Redis
REDIS_URL=redis://redis-dev:6379/0

# Flask
SECRET_KEY=dev-secret-key-change-in-production
FLASK_ENV=development
FLASK_DEBUG=1

# Celery
CELERY_BROKER_URL=redis://redis-dev:6379/0
CELERY_RESULT_BACKEND=redis://redis-dev:6379/0

# API Keys (set your actual keys)
XAI_API_KEY=your-xai-api-key-here
GROK_API_URL=https://api.x.ai/v1

# CORS
CORS_ORIGINS=http://localhost:3000,http://localhost:8080

# Logging
LOG_LEVEL=DEBUG

# Development Tools
DEV_DASHBOARD_PORT=8080
PGADMIN_PORT=8081
REDIS_COMMANDER_PORT=8082
EOF
        log_success "Created .env.dev file"
        log_warning "Please update XAI_API_KEY in .env.dev with your actual API key"
    else
        log_info ".env.dev file already exists"
    fi
}

